# Reglas del juego Piedra, Papel o Tijera:

# El juego se juega entre dos participantes.

# Cada jugador elige simultáneamente una de tres opciones: piedra, papel o tijera.

# La piedra vence a la tijera (la rompe).
# El papel vence a la piedra (la envuelve).
# La tijera vence al papel (lo corta).

# Si ambos jugadores eligen la misma opción, es un empate.

# Para determinar un ganador en una ronda:
#   - Si un jugador elige piedra y el otro tijera, gana la piedra.
#   - Si un jugador elige papel y el otro piedra, gana el papel.
#   - Si un jugador elige tijera y el otro papel, gana la tijera.

# El juego puede jugarse a una sola ronda o a varias rondas para determinar un ganador general (por ejemplo, el mejor de tres).