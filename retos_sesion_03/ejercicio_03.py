print("Tutorial: Cómo Barrer una Habitación de Forma Efectiva")
print("-" * 40)

print("\n**Paso 1: Reúne tus herramientas.**")
print("Necesitarás una escoba, un recogedor y una bolsa de basura (opcional).")

print("\n**Paso 2: Prepara la habitación.**")
print("Abre ventanas para ventilar y levanta objetos pequeños del suelo (zapatos, juguetes, etc.).")

print("\n**Paso 3: Comienza por los bordes.**")
print("Empieza a barrer desde las esquinas y los bordes de la habitación hacia el centro.")
print("Usa movimientos suaves y largos para evitar levantar mucho polvo.")

print("\n**Paso 4: Barre en secciones.**")
print("Divide mentalmente la habitación en secciones y barre cada una de ellas sistemáticamente.")
print("Superpón ligeramente cada pasada para no dejar áreas sin barrer.")

print("\n**Paso 5: Junta la suciedad en un montón.**")
print("Dirige toda la suciedad y el polvo hacia un punto central de la habitación.")

print("\n**Paso 6: Usa el recogedor.**")
print("Coloca el recogedor plano sobre el suelo, justo delante del montón de suciedad.")
print("Con cuidado, barre la suciedad dentro del recogedor.")

print("\n**Paso 7: Desecha la suciedad.**")
print("Vierte la suciedad del recogedor en una bolsa de basura.")

print("\n**Paso 8: Barre las áreas difíciles (opcional).**")
print("Usa la escoba de lado o con las cerdas para llegar a rincones estrechos o debajo de muebles bajos.")

print("\n**Paso 9: Limpia tus herramientas.**")
print("Sacude la escoba al aire libre o sobre la bolsa de basura para quitar el polvo restante.")

print("\n**¡Habitación barrida!**")
print("Disfruta de tu espacio limpio.")